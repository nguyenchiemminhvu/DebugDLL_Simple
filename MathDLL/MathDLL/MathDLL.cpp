#include "MathDLL.h"

#include <iostream>
using namespace std;

void Add(int a, int b)
{
	cout << a << " + " << b << " = " << a + b << endl;
}

void Sub(int a, int b)
{
	cout << a << " - " << b << " = " << a - b << endl;
}

void Mul(int a, int b)
{
	cout << a << " * " << b << " = " << a * b << endl;
}

void Div(int a, int b)
{
	cout << a << " / " << b << " = " << a / b << endl;
}